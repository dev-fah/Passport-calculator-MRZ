def char_to_value(c: str) -> int:
    c = c.upper()
    if c.isdigit():
        return int(c)
    if 'A' <= c <= 'Z':
        return ord(c) - ord('A') + 10
    if c == '<':
        return 0
    return 0

def calc_checksum(s: str) -> int:
    poids = [7, 3, 1]
    total = 0
    for i, ch in enumerate(s):
        total += char_to_value(ch) * poids[i % 3]
    return total % 10

def normalize_and_pad(passport, birth, expiry, optional):
    passport = passport.strip().upper()
    birth = birth.strip()
    expiry = expiry.strip()
    optional = optional.strip().upper()
    # Basic padding to MRZ lengths
    passport = passport.ljust(9, '<')[:9]
    optional = optional.ljust(14, '<')[:14]
    return passport, birth, expiry, optional

def simple_validate(passport, birth, expiry, optional):
    errors = []
    if not re.fullmatch(r'[A-Z0-9<]{1,9}', passport.strip().upper()):
        errors.append("Passport doit contenir 1–9 caractères A‑Z, 0‑9 ou <.")
    if not re.fullmatch(r'\d{6}', birth.strip()):
        errors.append("Birth doit être au format YYMMDD (6 chiffres).")
    if not re.fullmatch(r'\d{6}', expiry.strip()):
        errors.append("Expiry doit être au format YYMMDD (6 chiffres).")
    if not re.fullmatch(r'[A-Z<]{0,14}', optional.strip().upper()):
        errors.append("Optional doit contenir au plus 14 caractères A‑Z ou <.")
    return errors

